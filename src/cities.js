export function getCitySlug(cityId) {
  const ret = `https://api.teleport.org/api/urban_areas/slug:${cityId}/`;
  return ret;
}

export async function getCityName(cityId) {
  if (typeof cityId === "number") cityId = cityIds[cityId];
  const res = await (await fetch(getCitySlug(cityId))).json();
  return res.name;
}

export async function getImageURL(cityId) {
  const raw = await fetch(
    `https://api.teleport.org/api/urban_areas/slug:${cityId}/images/`
  );
  const res = raw.json();
  const ret = {
    webURL: res.photos[0].image.web,
    photographer: res.photos[0].attribution.photographer,
  };

  return ret;
}
